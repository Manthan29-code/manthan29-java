class Type_casting{
	public static void main(String... args){
		int a=45;
		double b=78.93647;
                char c='r';
		System.out.println("value of a: "+ a);
		System.out.println("value of b: "+ b);
		System.out.println("value of c: "+ c);
		System.out.println("value of (float)a: "+ (float)a);
		System.out.println("value of (char)a: "+(char) a);
		System.out.println("value of (float)c: "+ (float)c);
		System.out.println("value of (int)c: "+  (int)c);
		System.out.println("value of (int)b: "+ (int)b);

	}//main
    
}//class